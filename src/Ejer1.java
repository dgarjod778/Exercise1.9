
public class Ejer1 {

	public static void main(String[] args) {
		/*
		 * Investiga en la API de Java para qué sirven los siguientes métodos de la
		 * clase Math. Hacen el Eclipse un ejemplo de utilización para cada uno
		 */

		// 1.-
		System.out.println(Math.abs(-5)); // Me da un numero absoluto.

		// 2.-
		System.out.println(Math.addExact(5, 10)); // suma los dos numeros

		// 3.-
		System.out.println(Math.ceil(2.2)); // redondea los numeros.
		System.out.println(Math.ceil(-0.2));

		// 4.-
		System.out.println(Math.decrementExact(-2)); // decrece en uno el numero.

		// 5.-
		System.out.println(Math.floor(10));

		// 6.-
		System.out.println(Math.incrementExact(5));

		// 7.-
		System.out.println(Math.max(6, 5));

		// 8.-
		System.out.println(Math.multiplyExact(2, 4));

		// 9.-
		System.out.println(Math.negateExact(-10));

	}

}
